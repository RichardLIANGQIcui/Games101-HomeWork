//在半球上均匀采样，这里默认的半球是在局部坐标系且法线方向为（0,0,1）上的，而不是法线方向为N的坐标系，所以后续需要转换
Vector3f Material::sample(const Vector3f& wi, const Vector3f& N) {
    switch (m_type) {
    case DIFFUSE:
    case Microfacet:
    {
        // uniform sample on the hemisphere
        float x_1 = get_random_float(), x_2 = get_random_float();
        float z = std::fabs(1.0f - 2.0f * x_1);//随机半球方向的z轴向量
        float r = std::sqrt(1.0f - z * z), phi = 2 * M_PI * x_2;//再定一个随机角度
        Vector3f localRay(r * std::cos(phi), r * std::sin(phi), z);
        return toWorld(localRay, N);//转换到世界坐标

        break;
    }
    }
}

float Material::pdf(const Vector3f& wi, const Vector3f& wo, const Vector3f& N) {
    switch (m_type) {
    case DIFFUSE:
    case Microfacet:
    {
        // uniform sample probability 1 / (2 * PI)
        if (dotProduct(wo, N) > 0.0f)
            return 0.5f / M_PI;
        else
            return 0.0f;
        break;
    }
    }
}



//微表面模型BRDF兼有漫反射和镜面反射项
//fr =kd*fd+ ks*fs
//fs= (D*G*F)/(4*(N点乘l）*（N点乘v））
//fd = c/PI,c为表面颜色,c一般取1，即为1.0/pI
//ks为反射率=F，kd为折射率=1-ks;

Vector3f Material::eval(const Vector3f& wi, const Vector3f& wo, const Vector3f& N) {
    switch (m_type) {
        case DIFFUSE://针对非微表面算法
        {
            //进来要先做一个判断，即N点乘wo不能小于0，不然没意义
            float cosalpha = dotProduct(N, wo);
            if (cosalpha > 0.0f)
            {
                Vector3f diffuse = Kd / M_PI;
                return diffuse;
            }
            else
            {
                return Vector3f(0.0f);
            }
            break;
        }

        case Microfacet:
        {
            float cosalpha = dotProduct(N, wo);
            if (cosalpha > 0.0f)
            {
                float roughness = 0.40;

                Vector3f V = -wi;//朝向像素方向
                Vector3f L = wo;//反射方向
                Vector3f H = normalize(V + L);

                float D = DistributionGGX(N, H, roughness);

                float G = GeometrySmith(N, V, L, roughness);

                float F;
                float etat = 1.85;
                fresnel(wi, N, etat, F);

                Vector3f nominator = D * F * G;
                float denominator = 4 * max(dotProduct(N, V), 0.0f) * max(dotProduct(N, L), 0.0f);

                Vector3f specular = nominator / max(denominator, 0.0001f);

                float ks_ = F;
                float kd_ = 1 - ks_;

                Vector3f diffuse = 1.0f / M_PI;

                return kd_ * diffuse + ks_ * specular;

            }
            else
            {
                return Vector3f(0.0f);
            }
            break;
        }
    }
}

